package Core_Java._2_Operators;

public class _5__Logical_Operators {
    public static void main(String[] args) {

//        Logical Not
        boolean a=10>20;
        boolean b=10<20;
        System.out.println(a&&b);
        System.out.println(a||b);
        System.out.println(!(a&&b));

    }
}
