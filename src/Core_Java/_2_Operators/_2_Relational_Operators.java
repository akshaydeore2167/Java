package Core_Java._2_Operators;

public class _2_Relational_Operators {
    public static void main(String[] args) {
        int x=2;
        int y=4;
        int z=5;

        //Smaller and Smaller than

        boolean r1=x<y;
        boolean r2=x<=y;

        //Greater and Greater than

        boolean r3=x>z;
        boolean r4=x>=z;

        //Equality

        boolean r5=x==y;
        boolean r6=x!=z;

        // Printing Results
        System.out.println(r1);
        System.out.println(r2);
        System.out.println(r3);
        System.out.println(r4);
        System.out.println(r5);
        System.out.println(r6);

    }
}
